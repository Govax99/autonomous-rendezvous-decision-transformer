function sat = create_satellite(L, x, R, p_dock)
    if nargin < 4
        p_dock = [L/2 0 0];
    end
    % points [8 x 3]
    sat.L = L;
    sat.points = L * [0.5 -0.5 0.5; ...
                  0.5 0.5 0.5; ...
                  0.5 0.5 -0.5; ...
                  0.5 -0.5 -0.5; ...
                  -0.5 -0.5 0.5; ...
                  -0.5 0.5 0.5; ...
                  -0.5 0.5 -0.5; ...
                  -0.5 -0.5 -0.5];
    sat.x = x; % [3 x N]
    sat.R = R; % [3 x 3 x N]
    sat.p_dock = p_dock;
end

